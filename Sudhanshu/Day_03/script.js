// Using var, let, and const
var name = "Gayathri"; 
let age = 25; 
const isDeveloper = true; // Constant (cannot be reassigned)

// Data Types
let number = 42; // Number
let text = "Hello, JavaScript!"; // String
let isValid = false; // Boolean
let skills = ["JavaScript", "React", "Node.js"]; // Array
let person = { name: "Gayathri", 
profession: "Developer" 
}; // Object

console.log(name, age, isDeveloper, number, text, isValid, skills, person);


// Operators

// Arithmetic Operators
let sum = 10 + 5; 
let product = 10 * 5; 
let division = 20 / 4; 
let remainder = 15 % 4; 

// Comparison Operators
console.log(10 > 5); 
console.log(10 === "10"); 
console.log(10 == "10"); 

// Logical Operators
console.log(true && false); 
console.log(true || false); 
console.log(!true); 

// Assignment Operators
let x = 5;
x += 3; 
console.log(x); 
