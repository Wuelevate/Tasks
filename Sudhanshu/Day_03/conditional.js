let score = 85;

// If-Else
if (score >= 90) {
    console.log("Grade: A");
} else if (score >= 80) {
    console.log("Grade: B");
} else {
    console.log("Grade: C");
}

// Switch Statement
let day = "Monday";
switch (day) {
    case "Monday":
        console.log("Start of the workweek!");
        break;
    case "Friday":
        console.log("Weekend is coming!");
        break;
    default:
        console.log("A regular day.");
}
